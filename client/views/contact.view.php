<script>
    function showAlert() {
        const alert = document.getElementById('alert');
        alert.classList.remove('hidden');
        setTimeout(() => {
            alert.classList.remove('opacity-0');
        }, 10);
    }

    function closeAlert() {
        const alert = document.getElementById('alert');
        alert.classList.add('opacity-0');
        setTimeout(() => {
            alert.classList.add('hidden');
        }, 500);
    }
</script>


